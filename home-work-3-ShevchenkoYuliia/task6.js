/**
Є додатні числа a та b (a > b).
На відрізку довжини a розміщено максимально можливу кількість відрізків довжини b (без накладення).
Не користуючись операціями добутку та ділення, знайти довжину вільної частини a.
 */
function lengthOfFreePart(a, b){
    while(a >= b){
        a -= b;
    }
    return a;
}

console.log("Довжина вільної частини a =", lengthOfFreePart(52, 6));